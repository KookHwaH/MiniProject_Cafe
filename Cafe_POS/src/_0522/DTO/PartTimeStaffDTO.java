package _0522.DTO;

public class PartTimeStaffDTO {
private int workday;
private int hour;
private int pay;

public int getWorkday() {
	return workday;
}
public void setWorkday(int workday) {
	this.workday = workday;
}
public int getHour() {
	return hour;
}
public void setHour(int hour) {
	this.hour = hour;
}
public int getPay() {
	return pay;
}
public void setPay(int pay) {
	this.pay = pay;
}
}
